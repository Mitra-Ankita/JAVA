public class Day3TernaryOperator 
{
   
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int max = (a > b) ? a : b;

        System.out.println("a = " + a); // Output: 10
        System.out.println("b = " + b); // Output: 20
        System.out.println("Maximum is: " + max); // Output: 20

    }
}


