public class Day5TwoDimensionalArray
 {  
    public static void main(String[] args) {
        // Declare and initialize a 2D array
        int[][] arr = {
            {1, 2},
            {3, 4}
        };

        // Print specific elements of the 2D array
        System.out.println(arr[0][0]); // Output: 1
        System.out.println(arr[1][1]); // Output: 4
    }
}


