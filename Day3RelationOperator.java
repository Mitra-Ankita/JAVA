public class Day3RelationOperator 
{
    public static void main(String[] args) {
        int a = 25;
        int b = 5;

        // Relational operations
        System.out.println("a = " + a); // Output: 25
        System.out.println("b = " + b); // Output: 5

        System.out.println("a == b : " + (a == b));   // Equal to, Output: false
        System.out.println("a != b : " + (a != b));   // Not equal to, Output: true
        System.out.println("a > b  : " + (a > b));    // Greater than, Output: true
        System.out.println("a < b  : " + (a < b));    // Less than, Output: false
        System.out.println("a >= b : " + (a >= b));   // Greater than or equal to, Output: true
        System.out.println("a <= b : " + (a <= b));   // Less than or equal to, Output: false
    }
}
// Relational Operator shows the result in true or false.

