public class Day4If 
{
    public static void main(String[] args) 
    {
        // Initialize the variable 'number' with the value 10
        int number = 10;

        // Check if the number is greater than 0 (positive)
        if (number > 0) 
        {
            // If the condition is true, print "Positive number"
            System.out.println("Positive number");
        }
    } 
}

// Output:
// Positive number
